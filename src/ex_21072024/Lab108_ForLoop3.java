package ex_21072024;

public class Lab108_ForLoop3 {

    public static void main(String[] args) {

        for (int _1 = 0; _1 <= 10; _1++) {
            System.out.println(_1);// 0 to 10
        }

        for (int i = 0; i > 1 ; i++) {
            System.out.println(i);//No Output
        }

        for (int i = 0; i >= 1 ; i++) {
            System.out.println(i);//No Output
        }
    }
}

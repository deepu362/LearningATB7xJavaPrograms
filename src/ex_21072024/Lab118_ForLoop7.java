public class Lab118_ForLoop7 {
    public static void main(String[] args) {
        for (int i = 0; i < 10 ; i++) { // 0 to 9
            System.out.println("Pramod");
        }

        for (int i = 1; i <= 10 ; i++) { // 1 to 10
            System.out.println("Dutta");
        }
    }
}

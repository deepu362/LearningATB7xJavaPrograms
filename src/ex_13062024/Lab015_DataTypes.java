package ex_13062024;

public class Lab015_DataTypes {
    public static void main(String[] args) {

        //Data Type
        //Primitive Data Type
        //boolean
            //true or false
        boolean is_married = true;
        boolean are_you_a_dog = false;

        System.out.println(is_married);
        System.out.println(are_you_a_dog);

        //Numerical
            //Integral - byte, short, int, long

        byte b = 100;
        short s = 101;
        int age = 130;
        long phone_number = 9876543210l;

            //char - char - A, B
        char grade = 'A';

            //floating
                //float
        float pi = 3.14f;
                //double
        double d = 118.654;

        //Non primitive Data types
        // String name -> "pramod" - number or char or bunch of chars
        String name = "Pramod";
        String name2 = "P";


    }
}

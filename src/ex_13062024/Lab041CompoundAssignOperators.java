package ex_13062024;

public class Lab041CompoundAssignOperators {
    public static void main(String[] args) {
        //Compound Assignment Operators

        int a = 10;
        //+=, -=, /=, *=, %=

        a+=10;//a=a+10
        a-=10;//a=a-10
        a/=10;//a=a/10
        a*=10;//a=a*10
    }
}

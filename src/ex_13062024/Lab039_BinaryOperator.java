package ex_13062024;

public class Lab039_BinaryOperator {

    public static void main(String[] args) {
        String firstName = "Deepika";
        String lastName = "Girdhar";
        System.out.println(firstName+lastName);//concatenate

        byte a = 10;
        byte b = 18;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);//Remainders
    }
}
